    private void cargarTabla() {

        //crea una instancia de la tabla modelo para cambiar la  
        //opcion de que pueda modificar algun dato
        //obligando si o si a apretar el btn modificar para hacerlo
        DefaultTableModel modeloTabla = new DefaultTableModel() {

            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }//retorna falso que pueda modificar, sino por defecto
            // la tabla puede modificarse los datos

        };//importante ;

        String titulos[] = {"id", "Modelo", "Marca", "Motor", "Patente", "Puertas"};
        modeloTabla.setColumnIdentifiers(titulos);

        //traer los autos desde la bd
        List<Automovil> listaAutomovil = control.traerAutos();

        if (listaAutomovil != null) { //si la lista no está vacia

            for (Automovil auto : listaAutomovil) {
                Object[] objeto = {auto.getId(), auto.getModelo(), auto.getMarca(),
                    auto.getMotor(), auto.getPatente(), auto.getCantPuertas()};

                modeloTabla.addRow(objeto);
            }
        }

        tablaAutos.setModel(modeloTabla); //cargo el modelo de tabla

    }

    public void mostrarMensaje(String mensaje, String tipo, String titulo) {

        JOptionPane optionPane = new JOptionPane(mensaje);

        if (tipo.equals("Info")) {
            optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);
        } else if (tipo.equals("Error")) {
            optionPane.setMessageType(JOptionPane.ERROR_MESSAGE);
        }
        JDialog dialog = optionPane.createDialog(titulo);
        dialog.setAlwaysOnTop(true);
        dialog.setVisible(true);
    }

