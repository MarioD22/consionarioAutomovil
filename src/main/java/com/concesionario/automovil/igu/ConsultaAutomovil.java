    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        //controlo que la tabla no esté vacia

        if (tablaAutos.getRowCount() > 0) {

            //valido que haya algo seleccionado de la tabla
            if (tablaAutos.getSelectedRow() != -1) {

                //Obtengo ID de auto a borrar
                int idAuto = Integer.parseInt(String.valueOf(tablaAutos.getValueAt(tablaAutos.getSelectedRow(), 0)));

                //creo funcion de borrar
                control.borrarAuto(idAuto);
                //JOptionPane.showMessageDialog(null, "Borrado con Exito");
                mostrarMensaje("Automovil Borrado Exitosamente!", "info", "Borrado exitoso");
                cargarTabla();

            } else {
                JOptionPane.showMessageDialog(null, "No hay nada seleccionado");
            }

        } else {
            JOptionPane.showMessageDialog(null, "tabla vacia");
        }


    }//GEN-LAST:event_btnEliminarActionPerformed

